function createCard(item) {
    const card = document.createElement('div');
    card.className = 'p-card';

    card.onclick = () => openActionModal(item);
    card.oncontextmenu = (e) => { 
        e.preventDefault(); 
        openContextModal(item); 
    };

    let starIcon = item.isHighlighted
        ? '<i class="ri-star-fill text-yellow"></i>'
        : '';

    const badgeColor = getCodeColor(item.produto);

    card.innerHTML = `
        <div class="p-card-header">
            <span class="p-badge" style="color: ${badgeColor}">
                #${item.codigo}
            </span>
            ${starIcon}
        </div>
        <div class="p-name">${item.produto}</div>
    `;

    return card;
}

function getCodeColor(produto) {
  const nome = produto.toLowerCase();

  if (nome.includes('caixa')) return '#684728'; // marrom
  if (nome.includes('rótulo') || nome.includes('rotulo')) return '#0178ff'; // azul
  if (nome.includes('diluente')) return '#7C3AED'; // roxo
  if (nome.includes('etiqueta')) return '#fabd15'; // amarelo
  if (nome.includes('tampa')) return '#3ab821'; // laranja
  if (nome.includes('filme')) return '#838891'; // cinza

  return 'var(--brand-red)'; // padrão
}


function render() {
    const searchTerms = normalizeString(currentSearch).split(/\s+/).filter(t => t.length > 0);

    const filtered = products.filter(p => {
        let matchFilter = false;
        if (currentFilter === 'all') matchFilter = true;
        else if (currentFilter === 'destaques') matchFilter = p.isHighlighted;
        else matchFilter = p.category === currentFilter;

        const pName = normalizeString(p.produto);
        const pCode = normalizeString(p.codigo);
        const matchSearch = searchTerms.every(term => pName.includes(term) || pCode.includes(term));
        
        return matchFilter && (searchTerms.length === 0 || matchSearch);
    });

    productsGrid.innerHTML = '';
    highlightsGrid.innerHTML = '';

    if (filtered.length === 0) {
        productsGrid.innerHTML = '<div style="grid-column:1/-1;color:#6b7280;text-align:center;">Nenhum produto encontrado.</div>';
    }

    filtered.forEach(item => {
        const card = createCard(item);
        if (currentFilter !== 'destaques' && item.isHighlighted) {
            const hCard = card.cloneNode(true);
            hCard.onclick = () => openActionModal(item);
            hCard.oncontextmenu = (e) => { e.preventDefault(); toggleHighlight(item.codigo); };
            highlightsGrid.appendChild(hCard);
        }
        productsGrid.appendChild(card);
    });
}

function toggleHighlight(code) {
    const index = highlights.indexOf(code);
    if (index > -1) highlights.splice(index, 1);
    else highlights.push(code);
    localStorage.setItem('savedHighlights', JSON.stringify(highlights));
    processData();
    render();
}

function setupFilters() {
    const categories = ['Rótulo', 'Diluente', 'Frasco', 'Etiqueta', 'Caixa', 'Tampa', 'Filme'];
    const container = document.getElementById('filterContainer');
    container.innerHTML = '';

    const btnAll = document.createElement('button');
    btnAll.className = 'filter-chip active'; 
    btnAll.textContent = 'Todos';
    btnAll.onclick = (e) => { updateActiveFilter(e.target); currentFilter = 'all'; render(); };
    container.appendChild(btnAll);

    const btnStar = document.createElement('button');
    btnStar.className = 'filter-chip';
    btnStar.innerHTML = '<i class="ri-star-fill text-yellow"></i> Destaques';
    btnStar.onclick = (e) => { updateActiveFilter(e.target.closest('button')); currentFilter = 'destaques'; render(); };
    container.appendChild(btnStar);

    categories.forEach(cat => {
        if(products.some(p => p.category === cat)) {
            const btn = document.createElement('button');
            btn.className = 'filter-chip';
            btn.textContent = cat;
            btn.onclick = (e) => { updateActiveFilter(e.target); currentFilter = cat; render(); };
            container.appendChild(btn);
        }
    });
}

function updateActiveFilter(targetBtn) {
    document.querySelectorAll('.filter-chip').forEach(b => b.classList.remove('active'));
    targetBtn.classList.add('active');
}

