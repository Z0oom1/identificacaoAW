// Normalização para busca inteligente sem acentos
function normalizeString(str) {
    return str.toString().toLowerCase()
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .trim();
}

// Categorização
function inferCategory(name) {
    const n = name.toUpperCase();
    if (n.includes('ROTULO')) return 'Rótulo';
    if (n.includes('DILUENTE')) return 'Diluente';
    if (n.includes('FRASCO')) return 'Frasco';
    if (n.includes('ETIQUETA')) return 'Etiqueta';
    if (n.includes('CAIXA') || n.includes('CX ')) return 'Caixa';
    if (n.includes('TAMPA')) return 'Tampa';
    if (n.includes('FILME')) return 'Filme';
}

function getDayOfYear() {
    const now = new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const diff = now - start;
    const oneDay = 1000 * 60 * 60 * 24;
    return Math.floor(diff / oneDay);
}

