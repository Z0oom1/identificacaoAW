// Lógica de Movimentação 360° e Zoom no Scroll
const sheet = document.querySelector('.sheet-viewport');
const overlay = document.querySelector('.print-overlay');
const body = document.body;

let isDragging = false;
let startX, startY;
let currentRotX = 0, currentRotY = 0;
let velX = 0, velY = 0;
let currentZoom = 0.6;
let resetTimer;

function applyTransform() {
    sheet.style.transform = `scale(${currentZoom}) rotateX(${currentRotX}deg) rotateY(${currentRotY}deg)`;
}

// Loop de física
function physicsLoop() {
    if (!isDragging) {
        velX *= 0.95;
        velY *= 0.95;
        currentRotY += velX;
        currentRotX += velY;
        applyTransform();
    }
    requestAnimationFrame(physicsLoop);
}
physicsLoop();

function startResetTimer() {
    clearTimeout(resetTimer);
    resetTimer = setTimeout(() => {
        if (!isDragging) {
            sheet.style.transition = "transform 1.2s cubic-bezier(0.22, 1, 0.36, 1)";
            currentRotX = 0; currentRotY = 0;
            velX = 0; velY = 0;
            applyTransform();
            setTimeout(() => sheet.style.transition = "none", 1200);
        }
    }, 2500);
}

// Bloqueio de seleção inteligente
overlay.addEventListener('mousedown', (e) => {
    // Se clicar no overlay OU na margem da folha (sheet-viewport) mas NÃO no conteúdo (official-sheet)
    if (e.target === overlay || e.target === sheet) {
        isDragging = true;
        startX = e.clientX;
        startY = e.clientY;
        
        // Desativa seleção de texto para não "sujar" o movimento
        body.style.userSelect = 'none';
        sheet.style.transition = "none";
        clearTimeout(resetTimer);
    }
});

window.addEventListener('mousemove', (e) => {
    if (!isDragging) return;

    const dx = e.clientX - startX;
    const dy = e.clientY - startY;

    velX = dx * 0.15;
    velY = -dy * 0.15;

    currentRotY += velX;
    currentRotX += velY;

    startX = e.clientX;
    startY = e.clientY;

    applyTransform();
});

window.addEventListener('mouseup', () => {
    if (isDragging) {
        isDragging = false;
        // Reativa a seleção de texto
        body.style.userSelect = 'auto';
        startResetTimer();
    }
});

// Zoom (Scroll)
overlay.addEventListener('wheel', (e) => {
    e.preventDefault();
    currentZoom += e.deltaY * -0.0008;
    currentZoom = Math.max(0.3, Math.min(1.8, currentZoom));
    applyTransform();
    startResetTimer();
}, { passive: false });

